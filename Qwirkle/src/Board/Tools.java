    private void updateTileSelector(){
        tileSelector.removeAllItems();
       ArrayList<String> tiles = pTiles.getHumanTiles();
       tiles.forEach(tile -> {
           if(tile != "n"){
           tileSelector.addItem(tile);
           }
       });
       setTileIcon();
    }
    
    private void setTileIcon(){
        String route = "./assets/" + tileSelector.getSelectedItem()+ ".png";
        BufferedImage image ;
        try {                
          image = ImageIO.read(new File(route));
       } catch (IOException ex) {
            System.out.println(ex);
            return;
       }
        icon.setIcon(new ImageIcon(image));
        this.revalidate();
    }
    

